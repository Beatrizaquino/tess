public class Animal {
    
    public String nome;
    public String raca;
    public int idade;

    public String getNome(){
        return this.nome;
    }

    public void setNome(String nome){
        nome = this.nome;
    }
    public String getRaca(){
        return this.raca;
    }

    public void setRaca(String raca){
        raca = this.raca;
    }
    public int getIdade(){
        return this.idade;
    }

    public void setIdade(int idade){
        idade = this.idade;
    }
}
